// online stock span;
class StockSpanner {
public:
    stack<pair<int,int>> st;
    StockSpanner() {
    }
    int next(int price) {
        int count=1;
        while(!st.empty() && price>=st.top().first){
            count=count+st.top().second;
            st.pop();
        }
        st.push({price,count});
        return count;
    }
};

